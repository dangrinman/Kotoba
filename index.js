function loadGame() {
    window.localStorage["nihongo"] = JSON.stringify(nihongo);
    window.localStorage.removeItem('selectOptionsLesson');
    window.localStorage.removeItem('selectOptionsType');
    window.localStorage.removeItem('poolSelectedItem');
    window.localStorage.removeItem('filtered');
}

function filterJSON() {
    const data = JSON.parse(window.localStorage["nihongo"]);
    const container = document.getElementById("containerNihongo");
    const total = document.getElementById("total");
    const hits = document.getElementById("hits");
    window.localStorage.removeItem('filtered');
    container.innerHTML = ""
    let filtered = [];

    const lesson = JSON.parse(window.localStorage["selectOptionsLesson"]);
    if (lesson) {
        for (let x = 0; x < lesson.length; x++) {
            filtered.push(...data.filter(it => it.lesson === lesson[x]));
        }
    }

    const type = window.localStorage["selectOptionsType"];
    if (type) {
        filtered = filtered.filter(it => it.type === type)
    }

    window.localStorage["filtered"] = JSON.stringify(filtered);
    total.innerHTML = `${filtered.length}`;
    hits.innerHTML = "0"
}

function setFilter(filter) {
    const selectElement = document.getElementById(filter);
    let valorSeleccionado = selectElement.value;
    if (filter === "selectOptionsLesson") {
        valorSeleccionado = `[${selectElement.value}]`
    }
    window.localStorage[filter] = valorSeleccionado;
}

function startGame() {
    filterJSON();
    createQuest();
}

function createQuest() {
    const dataFiltered = JSON.parse(window.localStorage["filtered"]);
    const maxAnswers = 4;
    const itemsSelected = []

    if (dataFiltered && dataFiltered.length > 3) {
        while (itemsSelected.length < maxAnswers) {
            var r = Math.floor(Math.random() * dataFiltered.length);
            if (itemsSelected.indexOf(dataFiltered[r]) === -1) itemsSelected.push(dataFiltered[r]);
        }

        // creamos el contenedor con la informacion
        const itemIndex = Math.floor(Math.random() * itemsSelected.length)
        const itemSelect = itemsSelected[itemIndex]
        const container = document.getElementById("containerNihongo");
        container.innerHTML = "";
        const div = document.createElement('div')
        div.className = "cardGame"
        div.id = "cardGame"
        div.dataset.idCard = itemSelect.jp
        div.innerHTML = `<div>${itemSelect.jp} // ${itemSelect.kj}</div>`;
        container.appendChild(div)
        createAnswers(itemsSelected)
    }
}

function createAnswers(itemsSelected) {
    const container = document.getElementById("containerNihongo");
    const div = document.createElement('div')
    div.className = "answersGame";
    div.id = "answersGame";
    div.innerHTML = '';
    container.appendChild(div)
    for (let x = 0; x < itemsSelected.length; x++) {
        buttonAnswer(itemsSelected[x])
    }

}

function buttonAnswer(itemSelected) {
    const container = document.getElementById("answersGame");
    const button = document.createElement('button')
    button.id = "answersGame" + itemSelected.jp;
    button.className = "answers";
    button.onclick = function () { checkAnswer(itemSelected) }
    button.innerHTML = itemSelected.esp
    button.dataset.idCard = itemSelected.jp
    container.appendChild(button)
}

function checkAnswer(itemSelected) {
    const dataFiltered = JSON.parse(window.localStorage["filtered"]);
    const container = document.getElementById("cardGame");
    const hits = document.getElementById("hits");
    const total = document.getElementById("total");
    const correctAnswer = container.dataset.idCard;
    if (correctAnswer.toString() === itemSelected.jp.toString() && parseInt(hits.innerHTML) < parseInt(total.innerHTML)) {
        if (dataFiltered.length > 4) {
            delCorrectAnswers(dataFiltered, dataFiltered.map(e => e.jp).indexOf(itemSelected.jp));
        }
        hits.innerHTML = parseInt(hits.innerHTML) + 1
        createQuest()
    }
}

function delCorrectAnswers(arr, index) {
    let frontPart = arr.slice(0, index);
    let lastPart = arr.slice(index + 1); // index to end of array
    window.localStorage["filtered"] = JSON.stringify([...frontPart, ...lastPart])
}


